class Solution {
public:
    bool possibleToStamp(vector<vector<int>> &grid, int stampHeight, int stampWidth) {
        int m = (int) grid.size(), n = (int) grid[0].size();
        vector<vector<int>> sum(m + 1, vector<int>(n + 1, 0)), diff(m + 1, vector<int>(n + 1, 0)), sumDiff(m + 1,
                                                                                                           vector<int>(
                                                                                                                   n +
                                                                                                                   1,
                                                                                                                   0));
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                sum[i + 1][j + 1] = sum[i][j + 1] + sum[i + 1][j] - sum[i][j] + grid[i][j];
            }
        }
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                int k = i + stampHeight, l = j + stampWidth;
                if (k <= m && l <= n && sum[k][l] - sum[k][j] - sum[i][l] + sum[i][j] == 0) {
                    diff[k][l]++;
                    diff[k][j]--;
                    diff[i][l]--;
                    diff[i][j]++;
                }
            }
        }
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                sumDiff[i + 1][j + 1] += sumDiff[i][j + 1] + sumDiff[i + 1][j] - sumDiff[i][j] + diff[i][j];
                if (grid[i][j] == 0 && sumDiff[i + 1][j + 1] == 0) {
                    return false;
                }
            }
        }
        return true;
    }
};

