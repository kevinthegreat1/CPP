class Solution {
public:
    int circleGame(const vector<vector<int>> &toys, vector<vector<int>> &circles, const int &r) {
        sort(circles.begin(), circles.end());
        int count = 0;
        for (const vector<int> &toy: toys) {
            if (toy[2] > r) {
                continue;
            }
            auto x1 = lower_bound(circles.begin(), circles.end(),
                                  vector<int>{toy[0] + toy[2] - r, toy[1] + toy[2] - r}),
                    x2 = upper_bound(circles.begin(), circles.end(),
                                     vector<int>{toy[0] - toy[2] + r, toy[1] - toy[2] + r});
            while (x1 != x2) {
                const vector<int> &circle = *x1++;
                if (pow(circle[0] - toy[0], 2) + pow(circle[1] - toy[1], 2) <= pow(r - toy[2], 2)) {
                    count++;
                    break;
                }
            }
        }
        return count;
    }
};

